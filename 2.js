// 7. Даны два числа, вывести порядковый номер меньшего из них
"use strict";
alert("Введите два числа для определения меньшего:");

let first_number = Number(prompt("певрое число:")), second_number = Number(prompt("второе число:"));
if (first_number < second_number) {
	alert("Меньшее число 1"); 
}	else {
	alert("Меньшее число 2");
};
